package main

import "fmt"

func main() {
	var b byte = 23
	fmt.Println(b)

	var s int16 = 3333
	fmt.Println(s)

	var i = 9999
	fmt.Println(i)

	var l int64 = 32323
	fmt.Println(l)

	var a int64 = 3232343433
	fmt.Println(a)

	var d = 333.33
	fmt.Println(d)

	var f float32 = 333.33
	fmt.Println(f)

	var arr = [...]int{1, 2, 3}
	fmt.Println(arr)

	arr2 := [...]int{1, 2, 3}
	fmt.Println(arr2)
}
